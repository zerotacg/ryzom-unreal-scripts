import unreal

asset_registry = unreal.AssetRegistryHelpers().get_asset_registry()

tile_texture_path = "/Game/ryzom/landscape/_texture_tiles"
tile_texture_filter = unreal.ARFilter(package_paths=[tile_texture_path],
                                      recursive_paths=True,
                                      class_paths=[unreal.TopLevelAssetPath("/Script/Engine", "Texture2D")])
tile_textures = asset_registry.get_assets(tile_texture_filter)


def change_tile_array(tile_array_path, tile_bank):
    property_name = "source_textures"

    asset_data = asset_registry.get_asset_by_object_path(tile_array_path)
    asset = asset_data.get_asset()
    texture_array = asset.get_editor_property(property_name)

    asset_count = len(tile_bank)
    text_label = "Adding Textures to tilebank {}"
    print("Adding Textures to tilebank {}".format(asset_data.asset_name))

    with unreal.ScopedSlowTask(asset_count, text_label.format("...")) as slow_task:
        slow_task.make_dialog(True)
        for tile_index, texture_path in tile_bank.items():
            if slow_task.should_cancel():
                break
            slow_task.enter_progress_frame(1, text_label.format(texture_path))
            texture_data = find_tile_texture(texture_path)
            if (texture_data != None):
                print("Setting Array Entry {} to {}".format(texture_array[tile_index], texture_data.asset_name))
                texture_array[tile_index] = texture_data.get_asset()


def find_tile_texture(package_name):
    found = list(filter(lambda asset_data: str(asset_data.package_name).endswith(package_name), tile_textures))
    if (len(found) == 1):
        return found[0]
    else:
        print("Not found {}".format(package_name))
        return None


